
/************************************************************************************************
 *
 * ランダムエンチャント対応装備判定.
 *
 *-----------------------------------------------------------------------------------------------
 *-----------------------------------------------------------------------------------------------
 * @return true:対応 false:非対応
 ************************************************************************************************/
function IsEnableRandomEnchant(itemId) {

	var wpnlv = ItemObjNew[itemId][ITEM_DATA_INDEX_WPNLV];
	var flag = (wpnlv * 10) % 10;

	if (GetRndOptTypeId(wpnlv) > 0) {
		return true;
	}


	return (flag & 1) == 1;
}





